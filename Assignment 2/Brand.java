class Brand {
    static void name() {
        System.out.println("41. The brand name is Nike.");
    }

    static void popularity() {
        name();
        System.out.println("42. Nike is one of the most popular sports brands.");
    }

    static void products() {
        popularity();
        System.out.println("43. Nike produces shoes, apparel, and accessories.");
    }

    static void tagline() {
        products();
        System.out.println("44. Nike's tagline is 'Just Do It'.");
    }
}