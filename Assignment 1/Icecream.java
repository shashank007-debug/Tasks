class Icecream {
    static void scoop() { 
		System.out.println("Scooping ice cream"); 
		}
    static void melt() { System.out.println("Ice cream is melting"); }
    static void freeze() { System.out.println("Freezing ice cream"); }
    static void addToppings() { System.out.println("Adding toppings to ice cream"); }
    static void serve() { System.out.println("Serving ice cream"); }
    static void changeFlavor() { 
		scoop();
        melt();
        freeze();
        addToppings();
        serve();
		System.out.println("Changing ice cream flavor"); 
	}
    static void makeSundae() { System.out.println("Making an ice cream sundae"); }
    static void pack() { System.out.println("Packing ice cream"); }
    static void eat() { System.out.println("Eating ice cream"); }
    static void enjoy() { System.out.println("Enjoying ice cream"); }
}
